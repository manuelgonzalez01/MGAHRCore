export default function useVacationLocale() {
  const { language } = useLanguageSettings();
  const locale = language === "en" ? "en" : "es";
  const currentCopy = copy[locale];

  function getMappedLabel(value, dictionary = {}) {
    if (!value) {
      return currentCopy.notAvailable;
    }

    return dictionary[value] || value;
  }

  function getSeverityLabel(value) {
    return getMappedLabel(value, {
      info: locale === "es" ? "Informativa" : "Informational",
      warning: locale === "es" ? "Advertencia" : "Warning",
      critical: locale === "es" ? "Critica" : "Critical",
      healthy: locale === "es" ? "Saludable" : "Healthy",
      watch: locale === "es" ? "En vigilancia" : "Watch",
    });
  }

  function getModeLabel(value) {
    return getMappedLabel(value, {
      standard: currentCopy.standard,
      partial: currentCopy.partial,
      split: currentCopy.split,
      amendment: currentCopy.amendment,
      retroactive: currentCopy.retroactive,
    });
  }

  function getCategoryLabel(value) {
    return getMappedLabel(value, {
      all: currentCopy.all,
      onsite: currentCopy.onsite,
      hybrid: currentCopy.hybrid,
      remote: currentCopy.remote,
    });
  }

  function getRoleLabel(value) {
    return getMappedLabel(value, {
      manager: locale === "es" ? "Jefatura" : "Manager",
      hr: locale === "es" ? "RRHH" : "HR",
    });
  }

  function getStepStateLabel(value) {
    return getMappedLabel(value, {
      pending: locale === "es" ? "Pendiente" : "Pending",
      approved: locale === "es" ? "Aprobada" : "Approved",
      rejected: locale === "es" ? "Rechazada" : "Rejected",
      returned: locale === "es" ? "Devuelta" : "Returned",
      delegated: locale === "es" ? "Delegada" : "Delegated",
    });
  }

  function getStatusValueLabel(value) {
    return getMappedLabel(value, {
      draft: currentCopy.draft,
      submitted: currentCopy.submitted,
      under_review: locale === "es" ? "En revision" : "Under review",
      pending_manager_approval: locale === "es" ? "Pendiente jefe" : "Pending manager",
      pending_hr_approval: locale === "es" ? "Pendiente RRHH" : "Pending HR",
      approved: locale === "es" ? "Aprobada" : "Approved",
      rejected: locale === "es" ? "Rechazada" : "Rejected",
      cancelled: locale === "es" ? "Cancelada" : "Cancelled",
      returned_for_changes: locale === "es" ? "Devuelta para cambios" : "Returned for changes",
      scheduled: locale === "es" ? "Programada" : "Scheduled",
      consumed: locale === "es" ? "Consumida" : "Consumed",
      expired: locale === "es" ? "Vencida" : "Expired",
      holiday: locale === "es" ? "Feriado" : "Holiday",
      active: locale === "es" ? "Activa" : "Active",
    });
  }

  function getActionLabel(value) {
    return getMappedLabel(value, {
      request_created: locale === "es" ? "Solicitud creada" : "Request created",
      manager_review: locale === "es" ? "Revision de jefatura" : "Manager review",
      request_updated: locale === "es" ? "Solicitud actualizada" : "Request updated",
      approval_delegated: locale === "es" ? "Aprobacion delegada" : "Approval delegated",
      approved: currentCopy.approve,
      rejected: currentCopy.reject,
      returned_for_changes: locale === "es" ? "Devuelta para cambios" : "Returned for changes",
      cancelled: locale === "es" ? "Cancelada" : "Cancelled",
      submitted: currentCopy.submitted,
      routed_to_review: locale === "es" ? "Enviada a revision" : "Routed to review",
      queued_for_manager: locale === "es" ? "En cola de jefatura" : "Queued for manager",
      scheduled: locale === "es" ? "Programada" : "Scheduled",
      consumed: locale === "es" ? "Consumida" : "Consumed",
      expired: locale === "es" ? "Vencida" : "Expired",
    });
  }

  function getOrgLabel(value) {
    return getMappedLabel(value, {
      "People & Culture": locale === "es" ? "Personas y Cultura" : "People & Culture",
      Finance: locale === "es" ? "Finanzas" : "Finance",
      "Finance Manager": locale === "es" ? "Gerente de Finanzas" : "Finance Manager",
      "HR Director": locale === "es" ? "Director de RRHH" : "HR Director",
      "HR Operations": locale === "es" ? "Operaciones de RRHH" : "HR Operations",
      "People Director": locale === "es" ? "Director de Personas" : "People Director",
      "Business Manager": locale === "es" ? "Gerente de Negocio" : "Business Manager",
      "Lider directo": locale === "es" ? "Lider directo" : "Direct manager",
      Closed: locale === "es" ? "Cerrado" : "Closed",
    });
  }

  function getEventTypeLabel(value) {
    return getMappedLabel(value, {
      holiday: locale === "es" ? "Feriado" : "Holiday",
      vacation: locale === "es" ? "Vacacion" : "Vacation",
      annual: locale === "es" ? "Anual" : "Annual",
      active_request: locale === "es" ? "Solicitud activa" : "Active request",
    });
  }

  function getMovementTypeLabel(value) {
    return getMappedLabel(value, {
      accrual: locale === "es" ? "Devengo" : "Accrual",
      carry_over: locale === "es" ? "Arrastre" : "Carry-over",
      reversal: locale === "es" ? "Reversion" : "Reversal",
      consumption: locale === "es" ? "Consumo" : "Consumption",
    });
  }

  return {
    language: locale,
    isSpanish: locale === "es",
    copy: currentCopy,
    getSeverityLabel,
    getModeLabel,
    getCategoryLabel,
    getRoleLabel,
    getStepStateLabel,
    getStatusValueLabel,
    getActionLabel,
    getOrgLabel,
    getEventTypeLabel,
    getMovementTypeLabel,
  };
}
